let _lang = localStorage.getItem("agri_lang") || "en";

/** Translate a key to the current language */
export function t(key) {
  return (LANG_DATA[_lang] || LANG_DATA.en)[key] ?? (LANG_DATA.en[key] ?? key);
}

/** Get current language code */
export function getLang() { return _lang; }

/** Get time-based greeting in current language */
export function getGreeting() {
  const h = new Date().getHours();
  if (h >= 5 && h < 12) return t("goodMorning");
  if (h >= 12 && h < 17) return t("goodAfternoon");
  if (h >= 17 && h < 21) return t("goodEvening");
  return t("goodNight");
}

/** Switch language and re-apply all translations */
export function setLanguage(lang) {
  if (!LANG_DATA[lang]) return;
  _lang = lang;
  localStorage.setItem("agri_lang", lang);
  applyTranslations();
  document.dispatchEvent(new CustomEvent("langchange", { detail: { lang } }));
}

/** Apply translations to all [data-i18n] elements */
export function applyTranslations() {
  document.querySelectorAll("[data-i18n]").forEach((el) => {
    const key = el.getAttribute("data-i18n");
    const text = t(key);
    if (text && text !== key) el.textContent = text;
  });
}

/** Expose to global scope for non-module scripts */
window.i18n = { t, getLang, setLanguage, getGreeting, applyTranslations };

